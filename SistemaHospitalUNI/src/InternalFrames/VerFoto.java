    public VerFoto(byte[] Arreglo) {
        initComponents();
        try{
            InputStream in = new ByteArrayInputStream(Arreglo);
            BufferedImage image = ImageIO.read(in);
            CargarFoto(new ImageIcon(image));
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this,"Error al Cargar Imagen","Error",JOptionPane.ERROR_MESSAGE);
        }
    }
    
public void CargarFoto(Icon icono){
        RedimensionarImagen redimImagen = new RedimensionarImagen();
        icono = redimImagen.imageToIcon(redimImagen.iconToBufferedImage(icono).getScaledInstance(this.jLabel1.getWidth(), this.jLabel1.getHeight(), 0));
        this.jLabel1.setIcon(icono);
}


