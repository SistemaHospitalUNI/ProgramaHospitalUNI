    public void llenarTabla(JTable tabla){
        
        DefaultTableModel modelo = new DefaultTableModel();
        DAO dao = new DAO(this.sf);
        modelo.addColumn("Id");
        modelo.addColumn("Barrio");
        modelo.addColumn("Distrito");
        
        lstSector = dao.Listar_Sectores();
        
        for (Sector s:lstSector) {
            Object[] fila = {s.getIdSector(),s.getBarrio(),s.getDistrito()};
            modelo.addRow(fila);
        }
        
        tabla.setModel(modelo);
        
    }

