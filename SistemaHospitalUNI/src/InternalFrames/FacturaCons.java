    private void pagotextfieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_pagotextfieldFocusLost
        try{
            float pago = Float.parseFloat(this.pagotextfield.getText());
            float total = Float.parseFloat(this.totaltextfield.getText());
            if(pago<total){JOptionPane.showMessageDialog(this, "Pago incorrecto","Error",JOptionPane.ERROR_MESSAGE);this.cambiotextfield.setText("");}
            else{
                this.cambiotextfield.setText(String.valueOf(pago-total));}
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Dato incorrecto","Error",JOptionPane.ERROR_MESSAGE);
            this.cambiotextfield.setText("0");
        }
    }//GEN-LAST:event_pagotextfieldFocusLost

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            String al = this.jTextField4.getText();

            if(Float.parseFloat(this.totaltextfield.getText())==0 || this.jTextField4.equals("")){
                JOptionPane.showMessageDialog(this, "Revise los datos intoducidos","Error",JOptionPane.ERROR_MESSAGE);
            }
            else{
                ComprobarConsulta();
                DAO d = new DAO(sf);
                FacturaConsulta fc = new FacturaConsulta();
                fc.setNumfactura(Integer.parseInt(this.n0factura.getText()));
                fc.setFecha(new Date());
                fc.setConsulta(d.busquedaConsultaId(Integer.parseInt(this.jTextField4.getText())));
                fc.setCajero(d.busquedaCajeroId(idcajero));
                fc.setTotal(Float.parseFloat(this.totaltextfield.getText()));
                fc.setPago(Float.parseFloat(this.pagotextfield.getText()));
                fc.setCambio(Float.parseFloat(this.cambiotextfield.getText()));
                int i =d.GuardarFacturaConsulta(fc);
                if(i!=-1){JOptionPane.showMessageDialog(this,"Facutra Registrada");Limpiar();}
                else{JOptionPane.showMessageDialog(this, "Error","Error al guardar Factura",JOptionPane.ERROR_MESSAGE);}
                }
        }catch(NumberFormatException ee){JOptionPane.showMessageDialog(this, "Dato incorrecto en el numero de paciente, introduzca un nÃºmero");}

    }//GEN-LAST:event_jButton4ActionPerformed

 public void CargarFoto(Icon icono){
        RedimensionarImagen redimImagen = new RedimensionarImagen();
        icono = redimImagen.imageToIcon(redimImagen.iconToBufferedImage(icono).getScaledInstance(190, 125, 0));
        this.lblimage.setIcon(icono);
}
